<?php
namespace App\Filament\Resources;

use App\Filament\Resources\SettingResource\Pages;
use App\Models\Setting;
use Filament\Forms;
use Filament\Tables;
use Filament\Resources\Resource;
use Filament\Forms\Form;
use Filament\Tables\Table;
use Filament\Forms\Components\{TextInput, Textarea, FileUpload};

class SettingResource extends Resource
{
    protected static ?string $model = Setting::class;
    protected static ?string $navigationIcon = 'heroicon-o-cog-6-tooth';
    protected static ?string $navigationGroup = 'Pengaturan';
    protected static ?string $label = 'Pengaturan Website';

    public static function form(Form $form): Form
    {
        return $form
            ->schema([
                TextInput::make('domain')->required(),
                TextInput::make('name')->label('Nama Website')->required(),
                TextInput::make('tag')->label('Tagline')->nullable(),

                FileUpload::make('icon')->label('Favicon')->directory('settings')->image(),
                FileUpload::make('logo')->label('Logo')->directory('settings')->image(),

                TextInput::make('action_button_text')->label('Teks Tombol Aksi'),
                TextInput::make('no_whatsapp')->label('Nomor WhatsApp'),
                TextInput::make('wa_message')->label('Pesan WhatsApp'),

                TextInput::make('meta_author')->label('Meta Author'),
                Textarea::make('meta_description')->label('Meta Description'),
                Textarea::make('meta_keywords')->label('Meta Keywords'),

                Textarea::make('additional_meta')->label('Additional Meta')->rows(4),
            ]);
    }

    public static function table(Table $table): Table
    {
        return $table
            ->columns([
                Tables\Columns\TextColumn::make('domain')->searchable(),
                Tables\Columns\TextColumn::make('name'),
                Tables\Columns\TextColumn::make('meta_author'),
                Tables\Columns\TextColumn::make('created_at')->dateTime('d M Y'),
            ])
            ->actions([
                Tables\Actions\EditAction::make(),
                Tables\Actions\DeleteAction::make(),
            ])
            ->bulkActions([
                Tables\Actions\DeleteBulkAction::make(),
            ]);
    }

    public static function getPages(): array
    {
        return [
            'index' => Pages\ListSettings::route('/'),
            'create' => Pages\CreateSetting::route('/create'),
            'edit' => Pages\EditSetting::route('/{record}/edit'),
        ];
    }
}
